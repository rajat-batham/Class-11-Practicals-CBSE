# Program 2 - This is a program to check which number is greater


# Taking input for two numbers
num1 = int(input("Enter first number: "))
num2 = int(input("Enter second number: "))

# Now, we check the if num1 is greater or not
if num1 > num2:
    # if num1 is greater, it will print num1 is greater
    print(num1, "is greater")
else:
    # if num1 is not greater than num2, then obviously num2 is greater than num1
    print(num2, "is greater")

