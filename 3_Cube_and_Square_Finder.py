# Program 3 - It's a program to find Cube & Square of any input number


# Taking input of number
num = int(input("Enter number: "))

# Making Square of number
square = num * num            # you can also use: square = num**2

# Making Cube of number
cube = num * num * num        # you can also use: cube = num**3

# Printing both Square and Cube
print("Squar:",square)
print("Cube:",cube)


